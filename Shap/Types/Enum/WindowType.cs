        /// </summary>
        ClassIndex,

        /// <summary>
        /// Location index window.
        /// </summary>
        LocationIndex,

        /// <summary>
        /// Analysis window.
        /// </summary>
        Analysis,

        /// <summary>
        /// Feedback window.
        /// </summary>
        Feedback
    }
}
