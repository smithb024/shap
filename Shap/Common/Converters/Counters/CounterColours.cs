namespace Shap.Common.Converters.Counters
{
    using System.Windows.Media;

    /// <summary>
    /// Static class used to store the common colours for the counters.
    /// </summary>
    public static class CounterColours
    {
        /// <summary>
        /// The colour used for a count of lots.
        /// </summary>
        public static Color Lots = Colors.Blue;

        /// <summary>
        /// The colour used for a count of many.
        /// </summary>
        public static Color Many = Colors.RoyalBlue;

        /// <summary>
        /// The colour used for a count of some.
        /// </summary>
        public static Color Some = Colors.DodgerBlue;

        /// <summary>
        /// The colour used for a count of few.
        /// </summary>
        public static Color Few = Colors.MediumSlateBlue;

        /// <summary>
        /// The colour used for a count of one.
        /// </summary>
        public static Color One = Colors.LightSlateGray;
    }
}